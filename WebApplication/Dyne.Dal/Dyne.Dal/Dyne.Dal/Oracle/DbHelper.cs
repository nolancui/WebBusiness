        /// <summary>
        /// 打开数据库连接
        /// </summary>
        /// <param name="p_conn">数据库连接</param>
        private void Open(OracleConnection p_conn)
        {
            if (p_conn.State != ConnectionState.Open)
            {
                p_conn.Open();
            }
        }

        /// <summary>
        /// 关闭数据库连接
        /// </summary>
        /// <param name="p_conn">数据库连接</param>
        private void Close(OracleConnection p_conn)
        {
            if (p_conn.State == ConnectionState.Open)
            {
                p_conn.Close();
            }
        }

